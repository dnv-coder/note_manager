def append_notes_to_file(notes, filename):
    """
    Добавляет новые заметки в конец существующего файла.
    Если файл отсутствует, создаёт его.

    :param notes: Список заметок (каждая заметка — это словарь)
    :param filename: Имя файла для записи заметок
    """
    with open(filename, "a", encoding="utf-8") as f:
        for note in notes:
            # Преобразуем заметку в текстовый формат
            note_text = "\n".join([f"{key}: {value}" for key, value in note.items()])
            f.write(note_text + "\n" + "-"*40 + "\n")  # Добавляем разделитель между заметками

    print(f"{len(notes)} заметка(и) успешно добавлены в файл '{filename}'.")

